package G_Literals;

public class a_hex_octal_binary {
    public static void main(String[] args) {
        int decimalNum=110;//no prefex ---- decaimal
        int hexNum=0x6E;//0x--hex
        int ocatalNum=0156;//0----ocatal
        int ocatalNum2=0156;//64
        int binaryNum=0b1101110;//0b-binary

        System.out.println(decimalNum);
        System.out.println(hexNum);
        System.out.println(ocatalNum);
        System.out.println(ocatalNum2);
        System.out.println(binaryNum);
    }
    
}
